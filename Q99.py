from abc import ABC, abstractmethod

# Use the abc module to define an abstract base class Shape with an
# abstract method area(). Implement subclasses Circle and Rectangle.
class Shape(ABC):
    @abstractmethod
    def area(self):
        pass

class Circle(Shape):
    def __init__(self, radius):
        self.radius = radius

    def area(self):
        return 3.14 * self.radius * self.radius

class Rectangle(Shape):
    def __init__(self, width, height):
        self.width = width
        self.height = height

    def area(self):
        return self.width * self.height